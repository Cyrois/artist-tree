// Add generated avatar images to each artist
export const artists: Artist[] = artistsData.map((artist, index) => ({
  ...artist,
  image: getAvatarUrl(artist.name, index),
}));

// Helper functions for data access
export function getArtists(): Artist[] {
  return artists;
}

export function getArtistById(id: number): Artist | undefined {
  return artists.find((a) => a.id === id);
}

export function searchArtists(query: string): Artist[] {
  const lowerQuery = query.toLowerCase();
  return artists.filter(
    (a) =>
      a.name.toLowerCase().includes(lowerQuery) ||
      a.genre.some((g) => g.toLowerCase().includes(lowerQuery))
  );
}

export function filterArtistsByGenre(genres: string[]): Artist[] {
  if (genres.length === 0) return artists;
  return artists.filter((a) => a.genre.some((g) => genres.includes(g)));
}

export function filterArtistsByScoreRange(min: number, max: number): Artist[] {
  return artists.filter((a) => a.score >= min && a.score <= max);
}

export function sortArtists(by: 'score' | 'name' | 'listeners', direction: 'asc' | 'desc' = 'desc'): Artist[] {
  const sorted = [...artists].sort((a, b) => {
    switch (by) {
      case 'score':
        return a.score - b.score;
      case 'name':
        return a.name.localeCompare(b.name);
      case 'listeners':
        return a.spotifyListeners - b.spotifyListeners;
      default:
        return 0;
    }
  });
  return direction === 'desc' ? sorted.reverse() : sorted;
}

export function getSimilarArtists(artistId: number): Artist[] {
  const artist = getArtistById(artistId);
  if (!artist) return [];
  return artist.similarArtists.map((id) => getArtistById(id)).filter((a): a is Artist => a !== undefined);
}

export function getTrendingArtists(limit: number = 10): Artist[] {
  return sortArtists('listeners', 'desc').slice(0, limit);
}

export function getArtistsByIds(ids: number[]): Artist[] {
  return ids.map((id) => getArtistById(id)).filter((a): a is Artist => a !== undefined);
}
